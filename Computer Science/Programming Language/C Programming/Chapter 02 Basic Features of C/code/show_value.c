#include <stdio.h>

// 4. 编写一个程序来声明几个int 型和float 型变量，不对这些变
// 量进行初始化，然后显示它们的值。这些值是否有规律？（通常情况
// 下没有。）

//随机的值
int main(){
    int i, j ,k;
    float q, w, e;

    printf("i:%d\n",i);
    printf("j:%d\n",j);
    printf("k:%d\n",k);

    printf("q:%.2f\n",q);
    printf("w:%.2f\n",w);
    printf("e:%.2f\n",e);

    return 0;
}